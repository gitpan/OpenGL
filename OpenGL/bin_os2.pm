package OpenGL::bin_os2;
$VERSION = '0.01';
1;

=head1 NAME

OpenGL::bin_os2 - deliver the binary distribution version 0.01 of
module OpenGL version 0.4 for os2 and Perl version 5.00553
via CPAN.

=head1 SYNOPSYS

  perl -MCPAN -e "test 'OpenGL::bin_os2' "
  perl -MCPAN -e "install 'OpenGL::bin_os2' "

or

  perl -MCPAN -e shell
  CPAN> test OpenGL::bin_os2
  CPAN> install OpenGL::bin_os2

No additional software but a complete correctly installed Perl (binary)
distribution is required.

=head1 SEE ALSO

L<OpenGL>, L<CPAN>, L<perl>.

=cut
